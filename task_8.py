def parse_song(text):
    import re
    # Ğ Ğ°Ğ·Ğ´ĞµĞ»ÑĞµĞ¼ Ñ‚ĞµĞºÑÑ‚ Ğ½Ğ° Ñ‡Ğ°ÑÑ‚Ğ¸ Ñ Ğ¿Ğ¾Ğ¼Ğ¾Ñ‰ÑŒÑ Ñ€ĞµĞ³ÑƒĞ»ÑÑ€Ğ½Ñ‹Ñ… Ğ²Ñ‹Ñ€Ğ°Ğ¶ĞµĞ½Ğ¸Ğ¹
