# Инициализируем начальные координаты Петра
x = 0
y = 0

# Считываем количество ходов
N = int(input("Введите количество ходов Петра: "))

# Для каждого хода
for i in range(N):
    print(f"Ход {i+1}:")
    # Считываем количество шагов
    while True:
        try:
            steps = int(input("Введите количество шагов: "))
            if steps < 0:
                print("Количество шагов не может быть отрицательным. Пожалуйста, введите положительное число.")
            else:
                break
        except ValueError:
            print("Некорректный ввод. Пожалуйста, введите целое число.")
    # Считываем направление
    direction = input("Введите направление (влево, вправо, вверх, вниз): ").strip().lower()
    
    # Обновляем координаты в зависимости от направления
    if direction == 'вправо':
        x += steps
    elif direction == 'влево':
        x -= steps
    elif direction == 'вверх':
        y += steps
    elif direction == 'вниз':
        y -= steps
    else:
        print("Неизвестное направление. Попробуйте снова.")
        # Повторяем текущий ход
        continue

# Вычисляем минимальное количество шагов до конечной точки
min_steps = abs(x) + abs(y)

# Выводим результат
print(f"Минимальное количество шагов от (0,0) до конечной точки: {min_steps}")

